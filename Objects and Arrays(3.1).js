// A JavaScript object is a collection of key-value pairs, where keys are strings (called properties), and values can be data or functions.
//   Objects are used to store and organize data in a structured way.

// Example:
const person = {
  name: "John",
  age: 30,
  greet: function() {
    console.log("Hello!");
  }
};

console.log(person.name); // Outputs: John
person.greet(); // Outputs: Hello!
