// Selecting Elements JavaScript provides several methods to select elements from the DOM.

// getElementById: Selects an element by its id
// Example 
const element = document.getElementById("myElement");

// getElementsByClassName: Selects elements by their class name (returns a collection).
// Example 
const elements = document.getElementsByClassName("myClass");

// getElementsByClassName: Selects elements by their class name (returns a collection).
// Example
const elements = document.getElementsByClassName("myClass");

// getElementsByTagName: Selects elements by their tag name (e.g., div, p).
// Example 
const elements = document.getElementsByTagName("div");

//querySelector: Selects the first element that matches a CSS selector.
// Example:
const element = document.querySelector(".myClass");

// querySelectorAll: Selects all elements that match a CSS selector (returns a NodeList).
// Example:
const elements = document.querySelectorAll("div.myClass");









