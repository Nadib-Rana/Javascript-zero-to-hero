// JavaScript functions are defined with the function keyword.
// Keyword: function declares a function.
// Name: myFunction is the name of the function.
// Parentheses: () can hold parameters if needed (e.g., function myFunction(param1, param2)).
// Curly Braces: {} contain the code to be executed when the function is called.
// Example:
   function myFunction() {
      console.log("Hello, World!");
   }

   myFunction(); // Calls the function and outputs: Hello, World!
