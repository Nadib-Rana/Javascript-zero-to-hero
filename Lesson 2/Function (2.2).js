// Keyword: const is used to declare a constant variable (myFunction in this case).
// Assignment: The = assigns a function to the variable myFunction.
// Anonymous function: function() {} creates an unnamed function (this is often called an anonymous function).

Example:
  const myFunction = function() {
  console.log("Hello from a function expression!");
};

myFunction(); // Calls the function and outputs: Hello from a function expression!
