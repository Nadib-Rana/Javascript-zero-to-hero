// Arrow function syntax: Instead of using the function keyword, arrow functions use () => {}.
// Assignment: You assign the arrow function to a variable, in this case, myFunction.
// Parentheses: () can hold parameters, just like traditional functions.
// Arrow (=>): Separates the parameters from the function body.

// Example:
  const myFunction = () => {
  console.log("Hello from an arrow function!");
  };

 myFunction(); // Outputs: Hello from an arrow function!

